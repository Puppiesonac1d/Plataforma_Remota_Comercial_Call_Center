                    txtDCTOOC.setText(txtDCTOOC.getText().substring(2));
                    txtCargosOC.setText(txtCargosOC.getText().substring(2));
                    txtSubtotalOC.setText(txtSubtotalOC.getText().substring(2));
                    txtIVAOC.setText(txtIVAOC.getText().substring(2));
