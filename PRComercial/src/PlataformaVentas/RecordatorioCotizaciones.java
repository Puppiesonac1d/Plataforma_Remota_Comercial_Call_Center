    private void cambioestado(int est) {
        try {
            String query = "";
            if (est != 0) {
                query = queryTabla
                        + "where rec.Estado = ?";
                PreparedStatement pst = cn.prepareStatement(query);
                pst.setInt(1, est);
                ResultSet rs = pst.executeQuery();
                tblRecordatorio.setModel(DbUtils.resultSetToTableModel(rs));
            } else {
                query = queryTabla;
                PreparedStatement pst = cn.prepareStatement(query);
                ResultSet rs = pst.executeQuery();
                tblRecordatorio.setModel(DbUtils.resultSetToTableModel(rs));
            }

            ocultarAjustarTabla();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

