    private void btnCambiarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarEstadoActionPerformed
        if (tblRecordatorio.getSelectedRow() != -1) {

            String respuesta = JOptionPane.showInputDialog(null, "Escriba Su codigo de autorización.", "Permisos de acción", JOptionPane.INFORMATION_MESSAGE);
            int id = Integer.parseInt(tblRecordatorio.getModel().getValueAt(tblRecordatorio.getSelectedRow(), 0).toString());

            if (lbCodigo.getText().equals(respuesta)) {
                JOptionPane.showMessageDialog(null, "Codigo de autorización Correcto.", "Correcto", JOptionPane.INFORMATION_MESSAGE);

                try {
                    String Comentario = "Se cambio el estado del recordatorio id nro." + id + " por el empleado con id nro. " + lblIDUsuario.getText();

                    String query = "INSERT INTO movimientosventas (IDUsuario,Movimiento) VALUES (?,?)";
                    PreparedStatement pst = cn.prepareStatement(query);
                    pst.setInt(1, Integer.parseInt(lblIDUsuario.getText()));
                    pst.setString(2, Comentario);
                    int up = pst.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

                if (cbCambiarEstado.getSelectedIndex() == 0) {
                    JOptionPane.showMessageDialog(null, "Por favor Seleccione un estado valido");
                } else {
                    int estado = cbCambiarEstado.getSelectedIndex();
                    int ID = Integer.parseInt(tblRecordatorio.getModel().getValueAt(tblRecordatorio.getSelectedRow(), 0).toString());
                    try {
                        String query = "UPDATE recordatorio_cotizacion SET Estado = ? WHERE IDRecordatorio_cotizacion = ? ";
                        PreparedStatement update = cn.prepareStatement(query);
                        update.setInt(1, estado);
                        update.setInt(2, ID);
                        int i = update.executeUpdate();
                        JOptionPane.showMessageDialog(null, "El Estado se ha cambiado correctamente");
                        ActualizarTabla();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Codigo de autorizacion invalido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para proceder.");
        }
    }//GEN-LAST:event_btnCambiarEstadoActionPerformed

