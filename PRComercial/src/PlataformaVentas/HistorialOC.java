    private void btnCrearOTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearOTActionPerformed
        try {
            DefaultTableModel modelo = (DefaultTableModel) tblHistorialOC3.getModel();
            int index = 0;
            index = tblHistorialOC3.getSelectedRow();
            int seleccion = Integer.parseInt(modelo.getValueAt(index, 0).toString());
            System.out.println(seleccion);

            CrearOT ot = new CrearOT();
            this.dispose();
            ot.lblCodigo.setText(lblCodigoMenu.getText());
            ot.txtConsultarOC.setText(Integer.toString(seleccion));
            ot.setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una OC -" + ex.getMessage());
        }
    }//GEN-LAST:event_btnCrearOTActionPerformed

