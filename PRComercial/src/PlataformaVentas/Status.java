    public void Actualizar() {
        try {
            String query = consultaTabla
                    + "where cot.idUsuario=?";
            String param = lblIDUsuario.getText();
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setString(1, param);
            ResultSet rs = pst.executeQuery();
            tblCotizacionesPendientes.setModel(DbUtils.resultSetToTableModel(rs));
            AjusteTabla();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

