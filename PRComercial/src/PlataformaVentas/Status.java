    private void btnGuardarRecordatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarRecordatorioActionPerformed
        if (tblCotizacionesPendientes.getSelectedRow() != -1) {

            String respuesta = JOptionPane.showInputDialog(null, "Escriba Su codigo de autorización.", "Permisos de acción", JOptionPane.INFORMATION_MESSAGE);
            int id = Integer.parseInt(tblCotizacionesPendientes.getModel().getValueAt(tblCotizacionesPendientes.getSelectedRow(), 0).toString());

            if (lbCodigo.getText().equals(respuesta)) {
                JOptionPane.showMessageDialog(null, "Codigo de autorización Correcto.", "Correcto", JOptionPane.INFORMATION_MESSAGE);

                try {
                    String Comentario = "Se creo un recordatorio para la cotizacion id nro." + id + " por el empleado con id nro. " + lblIDUsuario.getText();

                    String query = "INSERT INTO movimientosventas (IDUsuario,Movimiento) VALUES (?,?)";
                    PreparedStatement pst = cn.prepareStatement(query);
                    pst.setInt(1, Integer.parseInt(lblIDUsuario.getText()));
                    pst.setString(2, Comentario);
                    int up = pst.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

                try {
                    String observacion = jTextObservacion.getText();

                    jDateRecordatorio.setDateFormatString("yyyy-MM-dd");
                    String fec = ((JTextField) jDateRecordatorio.getDateEditor().getUiComponent()).getText().substring(0, 10);

                    String query = "insert into recordatorio_cotizacion (fecha_recordatorioCobranza, observacion, IDCotizacion, Estado) VALUES (?, ?, ?, 1)";
                    PreparedStatement pst = cn.prepareStatement(query);
                    pst.setString(1, fec);
                    pst.setString(2, observacion);
                    pst.setInt(3, id);
                    int up = pst.executeUpdate();
                    jDateRecordatorio.setDateFormatString("dd-MM-yyyy");
                    JOptionPane.showMessageDialog(null, "Recordatorio guardado.");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Codigo de autorizacion invalido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para proceder.");
        }
    }//GEN-LAST:event_btnGuardarRecordatorioActionPerformed

    private void btnRecordatoriosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecordatoriosActionPerformed
        try {
            RecordatorioCotizaciones rec = new RecordatorioCotizaciones();

            String query = "select rec.IDRecordatorio_cotizacion AS 'ID Recordatorio',\n"
                    + "rec.IDCotizacion as 'ID cotización',\n"
                    + "DATE_FORMAT(rec.fecha_recordatorioCobranza, '%d-%m-%Y') as 'Fecha Venc.',\n"
                    + "rec.observacion as 'Observacion',\n"
                    + "(CASE\n"
                    + "WHEN rec.Estado = 1 THEN 'Por vencer'\n"
                    + "WHEN rec.Estado = 2 THEN 'Realizado'\n"
                    + "WHEN rec.Estado = 3 THEN 'Vencido'\n"
                    + "END) AS 'Estado'\n"
                    + "from recordatorio_cotizacion rec JOIN cotizacion cot on cot.IDCotizacion = rec.IDCotizacion\n"
                    + "JOIN cliente cli ON cli.idCliente = cot.idCliente\n"
                    + "JOIN organizacion org ON cli.IDOrganizacion = org.IDOrganizacion\n";
            PreparedStatement pst = cn.prepareStatement(query);;
            ResultSet rs = pst.executeQuery();
            rec.tblRecordatorio.setModel(DbUtils.resultSetToTableModel(rs));
            rec.ocultarAjustarTabla();
            rec.lblIDUsuario.setText(lblIDUsuario.getText());
            rec.lbCodigo.setText(lbCodigo.getText());
            rec.show();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnRecordatoriosActionPerformed

